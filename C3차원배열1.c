#include <stdio.h>
int main(){
    int score[2][3][4] = {
        {{70,70,80,90},{100,60,80,70},{80,97,97,77},{87,90,60,60}},
        {{60,70,77,87},{97,98,99,60},{78,88,80,80},{60,70,99,99}}
    };
    int i,j,k;

    for (i=0;i<2;i++){
        printf("%d반 학샹 점수 : \n",i+1);
        for(j=0;j<3;j++){
            printf("%d번 학생 : ",j+1);
            for(k=0;k<4;k++){
                printf("%d ",score[i][j][k]);
            }
            printf("\n");
        }
        printf("\n");
    }
}